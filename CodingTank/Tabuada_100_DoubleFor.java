public class Tabuada_100_DoubleFor {

    public static void main (String [] args) {
        for (int j = 1; j <= 10; j++) {
            System.out.println( );
            for (int k = 1; k <= 10; k++) {
                System.out.println ( k + " x " + j + " = " + (j * k) + "\t");
            }
        }
    }
}
